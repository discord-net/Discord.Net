namespace Discord
{
    /// <summary>
    /// The visibility of the connected account.
    /// </summary>
    public enum ConnectionVisibility
    {
        /// <summary>
        ///     Invisible to everyone except the user themselves.
        /// </summary>
        None = 0,
        /// <summary>
        ///     Visible to everyone.
        /// </summary>
        Everyone = 1
    }
}
