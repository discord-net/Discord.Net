namespace Discord
{
    /// <summary>
    ///     Defines the available Discord user status.
    /// </summary>
    public enum UserStatus
    {
        /// <summary>
        ///     The user is offline.
        /// </summary>
        Offline,
        /// <summary>
        ///     The user is online.
        /// </summary>
        Online,
        /// <summary>
        ///     The user is idle.
        /// </summary>
        Idle,
        /// <summary>
        ///     The user is AFK.
        /// </summary>
        AFK,
        /// <summary>
        ///     The user is busy.
        /// </summary>
        DoNotDisturb,
        /// <summary>
        ///     The user is invisible.
        /// </summary>
        Invisible,
    }
}
