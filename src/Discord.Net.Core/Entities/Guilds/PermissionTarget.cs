namespace Discord
{
    /// <summary>
    ///     Specifies the target of the permission.
    /// </summary>
    public enum PermissionTarget
    {
        /// <summary>
        ///     The target of the permission is a role.
        /// </summary>
        Role = 0,
        /// <summary>
        ///     The target of the permission is a user.
        /// </summary>
        User = 1,
    }
}
